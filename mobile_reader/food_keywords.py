def find_food_matches(text: str) -> list[dict]:
    """
    e약은요 텍스트에서 음식 키워드 매칭.
    반환: [{ label, emoji, reason, evidence }, ...] (중복 라벨 제거)
    """
    if not text:
        return []
    matched: dict[str, dict] = {}
    for label, patterns, emoji, default_reason in FOOD_PATTERNS:
        for p in patterns:
            idx = text.find(p)
            if idx >= 0:
                # 매칭된 위치의 앞뒤 60자 문맥
                start = max(0, idx - 30)
                end = min(len(text), idx + len(p) + 30)
                evidence = text[start:end].strip()
                # 라벨당 첫 매칭만 보관
                if label not in matched:
                    matched[label] = {
                        "label":    label,
                        "emoji":    emoji,
                        "reason":   default_reason,
                        "evidence": evidence,
                    }
                break
    return list(matched.values())
