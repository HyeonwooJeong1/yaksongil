def normalize_name(name: str) -> str:
    """
    제조사 접두사 + 제형 접미사 제거 후 핵심 이름만 반환.
    잘못된 정규화 방지: 너무 짧아지면 원본 유지.
    """
    name = (name or "").strip()
    if not name:
        return ""

    original = name

    # 1) 제조사 접두사 제거
    for p in KOREAN_PHARMA_PREFIXES:
        if name.startswith(p) and len(name) > len(p) + 1:
            name = name[len(p):]
            break

    # 2) 제형 접미사 제거
    for s in DRUG_FORM_SUFFIXES:
        if name.endswith(s) and len(name) > len(s) + 1:
            name = name[:-len(s)]
            break

    name = name.strip()

    # 결과가 너무 짧으면 원본 유지
    if len(name) < 2:
        return original

    return name
