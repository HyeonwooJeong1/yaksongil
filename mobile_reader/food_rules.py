    """ATC 코드 → 매칭되는 음식 규칙 리스트 (긴 접두사 우선, 라벨 중복 제거)."""
    if not atc:
        return []
    atc = atc.upper()
    seen = set()
    result = []
    for prefix, label, emoji, reason, level, source in sorted(FOOD_RULES, key=lambda r: -len(r[0])):
        if atc.startswith(prefix) and label not in seen:
            seen.add(label)
            result.append({
                "label": label, "emoji": emoji, "reason": reason,
                "level": level, "source": source,
            })
    return result
